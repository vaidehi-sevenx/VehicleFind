package com.vehiclefind.data;

public class Bike extends Vehicle{
	private int Power;
	private int cc;
	private boolean discbreak;
	
	public int getPower() {
		return Power;
	}
	public int setPower() {
		return Power;
	}
	public int getCc() {
		return cc;
	}
	public void setCc(int cc) {
		this.cc = cc;
	}
	public boolean isDiscbreak() {
		return discbreak;
	}
	public void setDiscbreak(boolean discbreak) {
		this.discbreak = discbreak;
	}
	
	

}
